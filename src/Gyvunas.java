public class Gyvunas {
    private String vardas;
    private String amzius;
    private String svoris;


    public Gyvunas(String vardas, String amzius, String svoris) {
        this.vardas = vardas;
        this.amzius = amzius;
        this.svoris = svoris;
    }

    public String getVardas() {
        return vardas;
    }

    public void setVardas(String vardas) {
        this.vardas = vardas;
    }

    public String getAmzius() {
        return amzius;
    }

    public void setAmzius(String amzius) {
        this.amzius = amzius;
    }

    public String getSvoris() {
        return svoris;
    }

    public void setSvoris(String svoris) {
        this.svoris = svoris;
    }

}
